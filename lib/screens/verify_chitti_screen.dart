                    child: Text(
                      'Could not generate payment schedule.\nPlease check the start month format.',
                      textAlign: TextAlign.center,
                      style: GoogleFonts.inter(
                        fontSize: 13,
                        color: Theme.of(context).colorScheme.error,
                      ),
