  bool get isEditMode => widget.chittiData != null;
  String? get chittiId => widget.chittiData?['id'];

  @override
  void initState() {
    super.initState();

    // Initialize controllers with default or pre-filled values
    if (isEditMode) {
      _initializeForEdit();
    } else {
      _initializeForCreate();
    }

    _nameController.addListener(() {
      if (_nameAutoGenerated) {
        _nameAutoGenerated = false;
      }
    });
    _loadAppSettings();
  }

  void _initializeForCreate() {
    _nameController = TextEditingController(text: 'Gold Chitti');
    _maxSlotsController = TextEditingController(text: '70');
  }

  void _initializeForEdit() {
    final data = widget.chittiData!;

    // DEBUG: Print all data to identify null values
    print('DEBUG _initializeForEdit: data keys = ${data.keys.toList()}');
    print('DEBUG _initializeForEdit: name = ${data['name']}');
    print('DEBUG _initializeForEdit: duration = ${data['duration']}');
    print('DEBUG _initializeForEdit: goldOptionsV2 = ${data['goldOptionsV2']}');
    print('DEBUG _initializeForEdit: goldOptions = ${data['goldOptions']}');
    print('DEBUG _initializeForEdit: purityPrices = ${data['purityPrices']}');
    print(
      'DEBUG _initializeForEdit: goldOptionRewards = ${data['goldOptionRewards']}',
    );
    print('DEBUG _initializeForEdit: startMonth = ${data['startMonth']}');
    print('DEBUG _initializeForEdit: paymentDay = ${data['paymentDay']}');
    print('DEBUG _initializeForEdit: luckyDrawDay = ${data['luckyDrawDay']}');
    print('DEBUG _initializeForEdit: maxSlots = ${data['maxSlots']}');

    _nameController = TextEditingController(text: data['name'] ?? '');

    // Parse duration
    final rawDuration = data['duration'];
    if (rawDuration != null) {
      _duration = int.tryParse(rawDuration.toString()) ?? 12;
    }
    // Clamp duration to valid range
    _duration = _duration.clamp(_minDuration, _maxDuration);

    // Extract gold options from goldOptionsV2 or legacy goldOptions
    final rawOptionsV2 = data['goldOptionsV2'] as List?;
    if (rawOptionsV2 != null && rawOptionsV2.isNotEmpty) {
      // New format
      for (var opt in rawOptionsV2) {
        if (opt == null) continue; // Skip null entries
        try {
          final goldOption = GoldOption.fromMap(
            Map<String, dynamic>.from(opt as Map),
          );
          if (!_selectedGoldOptions.any((o) => o.id == goldOption.id)) {
            _selectedGoldOptions.add(goldOption);
          }
        } catch (e) {
          print('DEBUG: Error parsing gold option: $e');
        }
      }
      // Extract total costs per option (from goldOptions legacy format)
      final goldOptions = data['goldOptions'] as List?;
      if (goldOptions != null) {
        for (
          int i = 0;
          i < _selectedGoldOptions.length && i < goldOptions.length;
          i++
        ) {
          final opt = goldOptions[i] as Map?;
          if (opt != null) {
            final totalValue = (opt['total_value'] ?? 0).toDouble();
            _goldOptionTotalCostControllers[_selectedGoldOptions[i].id] =
                TextEditingController(
                  text: totalValue > 0 ? totalValue.toStringAsFixed(0) : '',
                )..addListener(() => setState(() {}));
          }
        }
      }
    } else {
      // Legacy format - try to convert
      final rawOptions = data['goldOptions'] as List? ?? [];
      final options = <Map<String, dynamic>>[];
      for (var e in rawOptions) {
        if (e == null) continue;
        try {
          options.add(Map<String, dynamic>.from(e as Map));
        } catch (_) {
          // Skip invalid entries
        }
      }

      if (options.isNotEmpty) {
        for (var opt in options) {
          // Parse weight - handle both numeric and string formats like "5g"
          num weight = 0;
          final weightVal = opt['weight'] ?? opt['grams'];
          if (weightVal is num) {
            weight = weightVal;
          } else if (weightVal is String) {
            weight = num.tryParse(weightVal.replaceAll('g', '').trim()) ?? 0;
          }

          if (weight > 0) {
            // Extract actual type and purity from data
            final type = opt['type']?.toString() ?? 'Coin';
            final purity = opt['purity']?.toString() ?? '22 Karat';

            final legacyOption = GoldOption(
              id: GoldOption.generateId(),
              type: type,
              purity: purity,
              weight: weight,
            );
            _selectedGoldOptions.add(legacyOption);
          }
        }

        // Create total cost controllers from legacy data
        for (
          var i = 0;
          i < _selectedGoldOptions.length && i < options.length;
          i++
        ) {
          final opt = options[i];
          final totalValue = (opt['total_value'] ?? 0).toDouble();
          _goldOptionTotalCostControllers[_selectedGoldOptions[i].id] =
              TextEditingController(
                text: totalValue > 0 ? totalValue.toStringAsFixed(0) : '',
              )..addListener(() => setState(() {}));
        }
      }
    }

    _maxSlotsController = TextEditingController(
      text: '${data['maxSlots'] ?? 70}',
    );
    _selectedStartMonth =
        data['startMonth'] ?? DateFormat('MMMM yyyy').format(DateTime.now());
    _paymentDay = '${data['paymentDay'] ?? 5}';
    _luckyDrawDay = '${data['luckyDrawDay'] ?? 10}';

    // Load per-option rewards
    final goldOptionRewards = data['goldOptionRewards'] as Map?;
    if (goldOptionRewards != null && goldOptionRewards.isNotEmpty) {
      // Get the reward keys in order
      final rewardKeys = goldOptionRewards.keys.toList();

      // Match rewards by position since legacy options get new IDs
      for (int i = 0; i < _selectedGoldOptions.length; i++) {
        final option = _selectedGoldOptions[i];

        // First try to find by exact ID match
        Map? rewardConfig = goldOptionRewards[option.id] as Map?;

        // If not found by ID, try to match by position
        if (rewardConfig == null && i < rewardKeys.length) {
          rewardConfig = goldOptionRewards[rewardKeys[i]] as Map?;
        }

        if (rewardConfig != null) {
          _goldOptionRewardEnabled[option.id] =
              rewardConfig['enabled'] ?? false;
          _goldOptionRewardType[option.id] =
              rewardConfig['type'] ?? 'Fixed Amount';
          _goldOptionRewardControllers[option.id] = TextEditingController(
            text:
                (rewardConfig['value'] ?? rewardConfig['calculatedAmount'] ?? 0)
                    .toString(),
          )..addListener(() => setState(() {}));
        }
      }
    } else {
      // Handle legacy global rewardConfig
      final legacyRewardConfig = data['rewardConfig'] as Map?;
      if (legacyRewardConfig != null && legacyRewardConfig['enabled'] == true) {
        // Apply legacy reward to all selected gold options
        for (var option in _selectedGoldOptions) {
          _goldOptionRewardEnabled[option.id] = true;
          _goldOptionRewardType[option.id] =
              legacyRewardConfig['type'] ?? 'Fixed Amount';
          _goldOptionRewardControllers[option.id] = TextEditingController(
            text: (legacyRewardConfig['value'] ?? 0).toString(),
          )..addListener(() => setState(() {}));
        }
      }
    }
  }

  Future<void> _loadAppSettings() async {
    final settings = await SettingsService().getAppSettings();
    final goldOptions = await SettingsService().getGoldOptionsV2();
    final goldTypes = await SettingsService().getGoldTypes();
    final goldPurities = await SettingsService().getGoldPurities();
    setState(() {
      _appSettings = settings;
      _availableGoldOptions = goldOptions;
      _goldTypes = goldTypes;
      _goldPurities = goldPurities;
    });
    // Only auto-generate name in create mode
    if (!isEditMode) {
      _generateChittiName();
    }
  }

  Future<void> _generateChittiName() async {
    if (_appSettings == null) return;

    final generator = ChittiNameGenerator();
    final generatedName = await generator.generateNextName(
      appSettings: _appSettings!,
      duration: _duration,
      startMonth: _selectedStartMonth,
    );

    setState(() {
      _nameController.text = generatedName;
      _nameAutoGenerated = true;
    });
  }

  @override
  void dispose() {
    _nameController.dispose();
    _maxSlotsController.dispose();
    // Dispose all total cost controllers
    for (var controller in _goldOptionTotalCostControllers.values) {
      controller.dispose();
    }
    // Dispose all reward controllers
    for (var controller in _goldOptionRewardControllers.values) {
      controller.dispose();
    }
    super.dispose();
  }

  String _calculateEndMonth() {
    // Parse "January 2024" format
    final parts = _selectedStartMonth.split(' ');
    if (parts.length != 2) return _selectedStartMonth;

    const months = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];
    final monthIndex = months.indexOf(parts[0]);
    final year = int.tryParse(parts[1]) ?? DateTime.now().year;

    if (monthIndex == -1) return _selectedStartMonth;

    final endMonthIndex = (monthIndex + _duration - 1) % 12;
    final endYear = year + ((monthIndex + _duration - 1) ~/ 12);

    return '${months[endMonthIndex]} $endYear';
  }

