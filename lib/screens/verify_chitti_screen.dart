  /// Build payment timeline showing all payment dates
  Widget _buildPaymentTimeline(BuildContext context, String currency) {
    final (monthDates, monthNames) = CurrencyUtils.generateMonthDates(
      widget.startMonth,
      widget.duration,
    );
    final paymentDay = int.tryParse(widget.paymentDay) ?? 5;

    // Get selected gold option for EMI calculation
    final selectedOption = widget.goldOptions.firstWhere(
      (o) => o.id == _selectedOptionIdForSchedule,
      orElse: () => widget.goldOptions.first,
    );
    final totalValue = widget.totalCosts[selectedOption.id] ?? 0.0;
    final emiBreakdown = CurrencyUtils.getEMIBreakdown(
      totalValue,
      widget.duration,
    );
    final firstMonthEMI = emiBreakdown['firstMonth'] as double;
    final regularEMI = emiBreakdown['regularMonths'] as double;

    final colorScheme = Theme.of(context).colorScheme;

    return Column(
      children: [
        // Gold Option Selector
        if (widget.goldOptions.length > 1) ...[
          SingleChildScrollView(
            scrollDirection: Axis.horizontal,
            padding: const EdgeInsets.only(bottom: 20, left: 4, right: 4),
            child: Row(
              children: widget.goldOptions.map((option) {
                final isSelected = option.id == _selectedOptionIdForSchedule;
                return Padding(
                  padding: const EdgeInsets.only(right: 8),
                  child: FilterChip(
                    selected: isSelected,
                    label: Text(
                      '${option.weight}g ${option.type}',
                      style: GoogleFonts.inter(
                        fontSize: 12,
                        fontWeight: isSelected
                            ? FontWeight.w700
                            : FontWeight.w500,
                        color: isSelected
                            ? colorScheme.onPrimary
                            : colorScheme.onSurface.withOpacity(0.6),
                      ),
                    ),
                    onSelected: (selected) {
                      if (selected) {
                        setState(() {
                          _selectedOptionIdForSchedule = option.id;
                        });
                      }
                    },
                    backgroundColor: colorScheme.surface,
                    selectedColor: colorScheme.primary,
                    checkmarkColor: colorScheme.onPrimary,
                    shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(12),
                      side: BorderSide(
                        color: isSelected
                            ? colorScheme.primary
                            : colorScheme.outline.withOpacity(0.1),
                      ),
                    ),
                    showCheckmark: false,
                    visualDensity: VisualDensity.compact,
                  ),
                );
              }).toList(),
            ),
          ),
        ],
        // Timeline header
        Container(
          padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
          decoration: BoxDecoration(
            color: Theme.of(context).colorScheme.surfaceContainerHighest,
            borderRadius: const BorderRadius.vertical(top: Radius.circular(8)),
          ),
          child: Row(
            children: [
              Expanded(
                flex: 2,
                child: Text('Month', style: _headerStyle(context)),
              ),
              Expanded(
                flex: 2,
                child: Text(
                  'Payment Due',
                  style: _headerStyle(context),
                  textAlign: TextAlign.center,
                ),
              ),
              Expanded(
                child: Text(
                  'EMI',
                  style: _headerStyle(context),
                  textAlign: TextAlign.end,
                ),
              ),
            ],
          ),
        ),
