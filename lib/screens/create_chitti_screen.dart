  Widget _buildRewardConfigForOption(BuildContext context, GoldOption option) {
    // Safely get or create controllers for this option
    if (!_goldOptionTotalCostControllers.containsKey(option.id)) {
      _goldOptionTotalCostControllers[option.id] = TextEditingController(
        text: '',
      )..addListener(() => setState(() {}));
    }
    if (!_goldOptionRewardControllers.containsKey(option.id)) {
      _goldOptionRewardControllers[option.id] = TextEditingController(text: '0')
        ..addListener(() => setState(() {}));
      _goldOptionRewardEnabled[option.id] = true; // Always enabled
      _goldOptionRewardType[option.id] = 'Fixed Amount'; // Only fixed amount
    }

    final colorScheme = Theme.of(context).colorScheme;
    final totalCostController = _goldOptionTotalCostControllers[option.id]!;
    final rewardController = _goldOptionRewardControllers[option.id]!;

    // Calculate EMI preview with remainder handling
    final totalCost = double.tryParse(totalCostController.text) ?? 0;
    final emiBreakdown = CurrencyUtils.getEMIBreakdown(totalCost, _duration);
    final firstMonthEMI = emiBreakdown['firstMonth'] as double;
    final regularEMI = emiBreakdown['regularMonths'] as double;
    final emiRemainder = emiBreakdown['remainder'] as double;
    final hasRemainder = emiBreakdown['hasRemainder'] as bool;
    final currency = SettingsService().getCurrencySymbol();

    // Reward amount IS the monthly discount (direct value, not divided)
    final monthlyDiscount = double.tryParse(rewardController.text) ?? 0;

    return Container(
      margin: const EdgeInsets.only(bottom: 20),
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: colorScheme.surface,
        borderRadius: BorderRadius.circular(20),
        border: Border.all(color: colorScheme.primary.withOpacity(0.1)),
        boxShadow: [
          BoxShadow(
            color: colorScheme.primary.withOpacity(0.02),
            blurRadius: 10,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Header with gold option label
          Text(
            option.displayLabel,
            style: GoogleFonts.inter(
              fontSize: 14,
              fontWeight: FontWeight.w600,
              color: Theme.of(context).colorScheme.onSurface,
            ),
          ),
          const SizedBox(height: 12),

          // Total Cost Input
          Row(
            children: [
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    _buildTextFieldLabel('Total Cost ($currency)'),
                    const SizedBox(height: 6),
                    _buildTextField(
                      context,
                      totalCostController,
                      isNumber: true,
                    ),
                  ],
                ),
              ),
              const SizedBox(width: 12),
              // Reward Amount Input
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    _buildTextFieldLabel('Monthly Discount ($currency)'),
                    const SizedBox(height: 6),
                    _buildTextField(context, rewardController, isNumber: true),
                  ],
                ),
              ),
            ],
          ),

          // EMI and Discount Preview
          if (totalCost > 0 || monthlyDiscount > 0) ...[
            const SizedBox(height: 12),
            Container(
              padding: const EdgeInsets.all(10),
              decoration: BoxDecoration(
                color: Theme.of(context).colorScheme.surfaceContainerHighest,
                borderRadius: BorderRadius.circular(8),
              ),
              child: Row(
                children: [
                  // Monthly EMI
                  if (totalCost > 0)
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            'Monthly EMI',
                            style: GoogleFonts.inter(
                              fontSize: 11,
                              color: Theme.of(
                                context,
                              ).colorScheme.onSurfaceVariant,
                            ),
                          ),
                          const SizedBox(height: 2),
                          Text(
                            '$currency${regularEMI.toStringAsFixed(0)}',
                            style: GoogleFonts.inter(
                              fontSize: 14,
                              fontWeight: FontWeight.w600,
                              color: Theme.of(context).colorScheme.primary,
                            ),
                          ),
                          if (hasRemainder)
                            Text(
                              '1st: $currency${firstMonthEMI.toStringAsFixed(0)} (+$currency${emiRemainder.toStringAsFixed(0)})',
                              style: GoogleFonts.inter(
                                fontSize: 10,
                                color: Colors.green.shade700,
                              ),
                            ),
                        ],
                      ),
                    ),
                  // Monthly Discount
                  if (monthlyDiscount > 0)
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            'Monthly Discount',
                            style: GoogleFonts.inter(
                              fontSize: 11,
                              color: Theme.of(
                                context,
                              ).colorScheme.onSurfaceVariant,
                            ),
                          ),
                          const SizedBox(height: 2),
                          Text(
                            '-$currency${monthlyDiscount.toStringAsFixed(0)}/mo',
                            style: GoogleFonts.inter(
                              fontSize: 14,
                              fontWeight: FontWeight.w600,
                              color: Colors.green.shade700,
                            ),
                          ),
                        ],
                      ),
                    ),
                  // EMI After Discount
                  if (totalCost > 0 && monthlyDiscount > 0)
                    Expanded(
