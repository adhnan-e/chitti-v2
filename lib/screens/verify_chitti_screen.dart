                                    ).colorScheme.primary.withOpacity(0.1),
                                    borderRadius: BorderRadius.circular(10),
                                  ),
                                  alignment: Alignment.center,
                                  child: Text(
                                    '${index + 1}',
                                    style: GoogleFonts.inter(
                                      fontSize: 13,
                                      fontWeight: FontWeight.w700,
                                      color: Theme.of(
                                        context,
                                      ).colorScheme.primary,
                                    ),
                                  ),
                                ),
                                const SizedBox(width: 12),
                                Expanded(
                                  child: Text(
                                    monthName,
                                    style: _cellStyle(
                                      context,
                                    ).copyWith(fontWeight: FontWeight.w600),
                                  ),
                                ),
                              ],
                            ),
                          ),
                          // Payment due date
                          Expanded(
                            flex: 2,
                            child: Text(
                              formattedDueDate,
                              style: _cellStyle(context).copyWith(
                                color: Theme.of(
                                  context,
                                ).colorScheme.onSurface.withOpacity(0.6),
                              ),
                              textAlign: TextAlign.center,
                            ),
                          ),
                          // EMI amount
                          Expanded(
                            child: Column(
                              crossAxisAlignment: CrossAxisAlignment.end,
                              children: [
                                Text(
                                  '$currency${emi.toStringAsFixed(0)}',
                                  style: _cellStyle(context).copyWith(
                                    fontWeight: FontWeight.w700,
                                    color: isFirstMonth
                                        ? Colors.green.shade800
                                        : Theme.of(
                                            context,
                                          ).colorScheme.onSurface,
                                  ),
                                ),
                                if (isFirstMonth &&
                                    (firstMonthEMI - regularEMI) > 0)
                                  Builder(
                                    builder: (context) {
                                      final remainder =
                                          firstMonthEMI - regularEMI;
                                      return Text(
                                        '+${remainder % 1 == 0 ? remainder.toInt().toString() : remainder.toStringAsFixed(2)}',
                                        style: GoogleFonts.inter(
                                          fontSize: 9,
                                          color: Colors.green.shade700,
                                        ),
                                      );
                                    },
                                  ),
                              ],
                            ),
                          ),
                        ],
                      ),
                    );
                  },
                ),
        ),
      ],
    );
  }

