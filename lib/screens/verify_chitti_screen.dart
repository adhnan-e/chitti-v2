  @override
  void initState() {
    super.initState();
    // Initialize with the first available option
    _selectedOptionIdForSchedule = widget.goldOptions.isNotEmpty
        ? widget.goldOptions.first.id
        : '';
  }

  String _calculateEndMonth() {
    final parts = widget.startMonth.split(' ');
    if (parts.length != 2) return widget.startMonth;

    const months = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];
    final monthIndex = months.indexOf(parts[0]);
    final year = int.tryParse(parts[1]) ?? DateTime.now().year;

    if (monthIndex == -1) return widget.startMonth;

    final endMonthIndex = (monthIndex + widget.duration - 1) % 12;
    final endYear = year + ((monthIndex + widget.duration - 1) ~/ 12);

    return '${months[endMonthIndex]} $endYear';
  }

  Future<void> _handleSubmit() async {
    setState(() => _isLoading = true);

    // Build gold options for storage (legacy format for compatibility)
    final goldOptionsData = <Map<String, dynamic>>[];
    for (final option in widget.goldOptions) {
      final totalValue = widget.totalCosts[option.id] ?? 0;
      final emiBreakdown = CurrencyUtils.getEMIBreakdown(
        totalValue,
        widget.duration,
      );
      goldOptionsData.add({
        'weight': '${option.weight}g',
        'grams': option.weight,
        'total_value': totalValue,
        'rate_per_gram': 0, // No longer used
        'price': (emiBreakdown['regularMonths'] as double)
            .round(), // Base EMI for display
        'firstMonthEMI': (emiBreakdown['firstMonth'] as double)
            .round(), // First month with remainder
        'emiRemainder': (emiBreakdown['remainder'] as double)
            .round(), // Remainder added to first month
        'type': option.type,
        'purity': option.purity,
      });
    }

    final chittiData = {
      'name': widget.name,
      'duration': widget.duration,
      'startMonth': widget.startMonth,
      'goldOptions': goldOptionsData,
      'goldOptionsV2': widget.goldOptions.map((o) => o.toMap()).toList(),
      'totalCosts': widget.totalCosts,
      'maxSlots': widget.maxSlots,
      'paymentDay': int.tryParse(widget.paymentDay) ?? 5,
      'luckyDrawDay': int.tryParse(widget.luckyDrawDay) ?? 10,
      'goldOptionRewards': widget.goldOptionRewards,
      'rewardConfig': null, // Clear legacy field
    };

    try {
      if (widget.isEditMode && widget.chittiId != null) {
        await ChittiService().updateChitti(widget.chittiId!, chittiData);
      } else {
        await ChittiService().createChitti(
          name: widget.name,
          duration: widget.duration,
          startMonth: widget.startMonth,
          goldOptions: goldOptionsData,
          maxSlots: widget.maxSlots,
          paymentDay: int.tryParse(widget.paymentDay) ?? 5,
          luckyDrawDay: int.tryParse(widget.luckyDrawDay) ?? 10,
          goldOptionRewards: widget.goldOptionRewards,
        );

        if (widget.nameAutoGenerated && widget.appSettings != null) {
          final newCounter = (widget.appSettings!['lastChittiNumber'] ?? 0) + 1;
          await SettingsService().updateAppSettings({
            'lastChittiNumber': newCounter,
          });
        }
      }

      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(
              widget.isEditMode
                  ? 'Chitti updated successfully!'
                  : 'Chitti created successfully!',
            ),
            backgroundColor: Colors.green,
          ),
        );
        Navigator.of(context).popUntil((route) => route.isFirst);
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error: $e'), backgroundColor: Colors.red),
        );
      }
    } finally {
      if (mounted) setState(() => _isLoading = false);
    }
  }

