  void _showMonthPicker(BuildContext context) {
    int displayYear = DateTime.now().year;
    final int minYear = 2020; // Allow selecting dates from 2020 onwards
    final int maxYear = DateTime.now().year + 10;

    final months = [
      'Jan',
      'Feb',
      'Mar',
      'Apr',
      'May',
      'Jun',
      'Jul',
      'Aug',
      'Sep',
      'Oct',
      'Nov',
      'Dec',
    ];

    // Helper to get full month name
    final fullMonthNames = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];

    showModalBottomSheet(
      context: context,
      backgroundColor: Theme.of(context).cardColor,
      shape: const RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
      ),
      builder: (BuildContext context) {
        return StatefulBuilder(
          builder: (BuildContext context, StateSetter setModalState) {
            return Container(
              padding: const EdgeInsets.all(16),
              height: 320,
              child: Column(
                children: [
                  // Year Selector
                  Row(
                    mainAxisAlignment: MainAxisAlignment.spaceBetween,
                    children: [
                      IconButton(
                        onPressed: displayYear > minYear
                            ? () => setModalState(() => displayYear--)
                            : null,
                        icon: Icon(
                          Icons.chevron_left,
                          color: displayYear > minYear ? null : Colors.grey,
                        ),
                      ),
                      Text(
                        displayYear.toString(),
                        style: GoogleFonts.inter(
                          fontSize: 18,
                          fontWeight: FontWeight.bold,
                        ),
                      ),
                      IconButton(
                        onPressed: displayYear < maxYear
                            ? () => setModalState(() => displayYear++)
                            : null,
                        icon: Icon(
                          Icons.chevron_right,
                          color: displayYear < maxYear ? null : Colors.grey,
                        ),
                      ),
                    ],
                  ),
                  const Divider(),
                  const SizedBox(height: 8),
                  // Month Grid
                  Expanded(
                    child: GridView.builder(
                      gridDelegate:
                          const SliverGridDelegateWithFixedCrossAxisCount(
                            crossAxisCount: 4,
                            childAspectRatio: 1.5,
                            crossAxisSpacing: 12,
                            mainAxisSpacing: 12,
                          ),
                      itemCount: 12,
                      itemBuilder: (context, index) {
                        // Allow selecting any month in any year within the minYear-maxYear range
                        // const isSelected = ... (optional logic if we want to highlight current selection)

                        return InkWell(
                          onTap: () {
                            setState(() {
                              _selectedStartMonth =
                                  '${fullMonthNames[index]} $displayYear';
                            });
                            _generateChittiName();
                            Navigator.pop(context);
                          },
                          child: Container(
                            alignment: Alignment.center,
                            decoration: BoxDecoration(
                              color: Theme.of(
                                context,
                              ).colorScheme.primaryContainer.withOpacity(0.3),
                              borderRadius: BorderRadius.circular(8),
                              border: Border.all(
                                color: Theme.of(
                                  context,
                                ).colorScheme.primary.withOpacity(0.3),
                              ),
                            ),
                            child: Text(
                              months[index],
                              style: GoogleFonts.inter(
                                color: Theme.of(context).colorScheme.onSurface,
                                fontWeight: FontWeight.w500,
                              ),
                            ),
                          ),
                        );
                      },
                    ),
                  ),
                ],
              ),
            );
          },
        );
      },
    );
  }
