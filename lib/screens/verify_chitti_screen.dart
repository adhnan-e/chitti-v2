                  itemBuilder: (context, index) {
                    final monthDate = monthDates[index];
                    final monthName = monthNames[index];
                    final paymentDueDate = DateTime(
                      monthDate.year,
                      monthDate.month,
                      paymentDay,
                    );
                    final formattedDueDate = DateFormat(
                      'd MMM yyyy',
