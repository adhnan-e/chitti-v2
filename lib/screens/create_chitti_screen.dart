  /// Update total cost controllers based on selected options
  void _updateTotalCostControllers() {
    // Get selected gold option IDs
    final selectedOptionIds = _selectedGoldOptions.map((opt) => opt.id).toSet();

    // Remove controllers for options no longer selected
    _goldOptionTotalCostControllers.keys
        .where((id) => !selectedOptionIds.contains(id))
        .toList()
        .forEach((id) {
          _goldOptionTotalCostControllers[id]?.dispose();
          _goldOptionTotalCostControllers.remove(id);
        });

    // Remove reward state for options no longer selected
    _goldOptionRewardEnabled.keys
        .where((id) => !selectedOptionIds.contains(id))
        .toList()
        .forEach((id) {
          _goldOptionRewardControllers[id]?.dispose();
          _goldOptionRewardEnabled.remove(id);
          _goldOptionRewardType.remove(id);
          _goldOptionRewardControllers.remove(id);
        });

    // Add controllers for new gold options
    for (final option in _selectedGoldOptions) {
      // Total cost controller
      if (!_goldOptionTotalCostControllers.containsKey(option.id)) {
        _goldOptionTotalCostControllers[option.id] = TextEditingController(
          text: '',
        )..addListener(() => setState(() {}));
      }

      // Reward state - always enabled by default, fixed amount only
      if (!_goldOptionRewardEnabled.containsKey(option.id)) {
        _goldOptionRewardEnabled[option.id] = true; // Always enabled
        _goldOptionRewardType[option.id] = 'Fixed Amount'; // Only fixed amount
        _goldOptionRewardControllers[option.id] = TextEditingController(
          text: '0',
        )..addListener(() => setState(() {}));
      }
    }

    setState(() {});
  }

  /// Get total costs as a map
  Map<String, double> _getTotalCosts() {
    final costs = <String, double>{};
    for (var option in _selectedGoldOptions) {
      costs[option.id] =
          double.tryParse(
            _goldOptionTotalCostControllers[option.id]?.text ?? '0',
          ) ??
          0;
    }
    return costs;
  }

  /// Get gold option rewards as a map
  Map<String, Map<String, dynamic>> _getGoldOptionRewards() {
    final rewards = <String, Map<String, dynamic>>{};
    final totalCosts = _getTotalCosts();

    for (var option in _selectedGoldOptions) {
      // Always enabled, only fixed amount
      final value =
          double.tryParse(
            _goldOptionRewardControllers[option.id]?.text ?? '0',
          ) ??
          0;

      rewards[option.id] = {
        'enabled': true, // Always enabled
        'type': 'Fixed Amount', // Only fixed amount
        'value': value,
        'calculatedAmount': value,
        'totalCost': totalCosts[option.id] ?? 0,
      };
    }

    return rewards;
  }

